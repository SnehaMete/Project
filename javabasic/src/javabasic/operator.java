package javabasic;

public class operator {
	
	public static void main(String[] args) {
		//variable 
		int a = 20, b = 10;
		System.out.println("a+b=" + (a+b));
		
		System.out.println("a-b=" + (a-b));
		System.out.println("a*b=" + (a*b));
		System.out.println("a/b=" + (a/b));
		System.out.println("a%b=" + (a%b));
	}

}
