public class Logical {
	public static void main(String[] args) {

	System.out.println( (10>3) && (12>7) );
	System.out.println( (10>3) && (12<7) );
	System.out.println( (10>3) ||( 12<7) );
	System.out.println( (10<3) || (12>7) );
	System.out.println( !(10==7) );
	System.out.println( !(5>7));
			
			
			
		
}
}